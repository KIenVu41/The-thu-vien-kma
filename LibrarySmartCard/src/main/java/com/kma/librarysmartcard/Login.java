    private void bt_cntActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_cntActionPerformed
        // TODO add your handling code here:
        if(!isConnect) {
            if(card.connectCard()) {
               isConnect = true;
               bt_cnt.setText("Ngắt kết nối");
               JOptionPane.showMessageDialog(null, "Kết nối với thẻ thành công");
               bt_cnt.setForeground(Color.WHITE);
               bt_cnt.setBackground(Color.RED);
               btnLogin.setEnabled(true);
               btnTao.setEnabled(true);
            }else {
                JOptionPane.showMessageDialog(this, "chua connect");
                isConnect = false;
                bt_cnt.setText("connect");
              
            }
        }else {
            if(card.disconnect()) {
                isConnect = false;
                bt_cnt.setText("Kết nối với thẻ");
                JOptionPane.showMessageDialog(null, "Ngắt kết nối");
                btnLogin.setEnabled(false);
                btnTao.setEnabled(false);
            }
        }
    }//GEN-LAST:event_bt_cntActionPerformed
       
