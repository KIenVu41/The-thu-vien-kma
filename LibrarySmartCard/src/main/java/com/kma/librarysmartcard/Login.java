    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
//        if(!isConnect) {
//            JOptionPane.showMessageDialog(null, "Chưa kết nối với thẻ");
//            return;
//        }
        String pinTxt = pin.getText().trim();
        if(validate.isEmpty(pinTxt)) {
            JOptionPane.showMessageDialog(null, "Không để trống mã PIN");
            return;
        }    
        if(validate.isContainSpace(pinTxt)) {
            JOptionPane.showMessageDialog(null, "PIN không chứa space");
            return;
        }
        if(!validate.pinRequiredLength(pinTxt)) {
            JOptionPane.showMessageDialog(null, "Độ dài pin là 4 kí tự");
            return;
        }
        if(!validate.isLegitPin(pinTxt)) {
            JOptionPane.showMessageDialog(null, "Pin chỉ chứa các số từ 0-9");
            return;
        }
        if(card.checkPin(pinTxt)) {
                modulusPubkey = card.getModulusPubkey();
                exponentPubkey = card.getExponentPubkey();
                try {
                    String id = gi.getID();   
                    System.out.println("id" + id);
                    if(repo.insertPubkey(id, modulusPubkey.toString(), exponentPubkey.toString())) {
                          JOptionPane.showMessageDialog(null, "ok");
                    }
                }catch(Exception e) {
                    e.printStackTrace();
                }
                              
                Main main = new Main();
                main.show();
                main.setVisible(true);
                main.setLocationRelativeTo(null);
                setVisible(false);
           
        } else {
            int time = card.getRemainingTries();
            JOptionPane.showMessageDialog(null, "PIN sai, còn " + time + " lần");
            if(time == 0) {
                //card.disconnect();
                btnLogin.setEnabled(false);
                btnTao.setEnabled(false);
                btnUnblock.setText("Mở khóa");
            }
        }
        
    }//GEN-LAST:event_btnLoginActionPerformed

